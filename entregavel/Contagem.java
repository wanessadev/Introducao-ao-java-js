public class Contagem {
    public static void main(String[] args) {
        // O laço começa em 1 e vai até 10, somando +1 a cada volta
        for (int i = 1; i <= 10; i++) {
            System.out.println("Número: " + i);
        }
        
        System.out.println("Contagem finalizada!");
    }
}
